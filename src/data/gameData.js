export function getSlotCost(upgradeId, unitTypeId) {
  const upgrade = ALL_UPGRADES[upgradeId];
  if (!upgrade) return 0;
  if (typeof upgrade.slotCost === 'number') return upgrade.slotCost;
  return upgrade.slotCost[unitTypeId] ?? upgrade.slotCost['medium'] ?? 1;
}

